// SpamViking — bench roster GENERATOR (build step, runs in Node, not on the
// hot path). Reads the authored bench/<id>.json files (the single source of
// truth) and emits _bench_roster.js — a plain ES module exporting the roster
// as a literal object. That generated module is EDGE-SAFE (no require, no
// createRequire, no JSON-import attributes — just JavaScript), so the runtime
// (_bench_v2.js, imported by edge functions) can import it anywhere.
//
// Run whenever the bench json changes:  node api/compiler/bench/build_roster.js
// The json stays the ONE source; _bench_roster.js is a derived artifact — never
// hand-edit it.
const fs = require("fs");
const path = require("path");

const DIR = __dirname; // api/compiler/bench
const MANIFEST = require("./index.js"); // { id: {json}, ... } — the authored source

// Emit to api/_bench_roster.js (next to _bench_v2.js, which imports it).
const OUT = path.join(DIR, "..", "..", "_bench_roster.js");

const header =
`// AUTO-GENERATED by compiler/bench/build_roster.js — DO NOT EDIT BY HAND.
// Source of truth = compiler/bench/<id>.json. Re-run the generator after any
// bench content change:  node api/compiler/bench/build_roster.js
// Edge-safe: plain ES module, no require / no JSON-import attributes.
export const BENCH_RAW = `;

const body = JSON.stringify(MANIFEST, null, 2);

fs.writeFileSync(OUT, header + body + ";\n");
console.log("wrote " + path.relative(path.join(DIR, "..", ".."), OUT) +
  " with " + Object.keys(MANIFEST).length + " characters");
